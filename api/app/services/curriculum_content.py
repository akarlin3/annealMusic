"""Authored v6.4 curriculum content — the editorial body of the slice.

This module is the single, reviewable source of the full curriculum: five
tracks, ~55 lesson specs, and the prerequisite edge list. The seed migration
(``0023``) and the end-to-end coherence test both consume it; nothing here runs
an LLM. Each lesson is authored as a :class:`LessonSpec`-shaped dict — the LLM
pipeline fills in the per-step prose at generation time.

Specs follow the field rules in ``docs/CURRICULUM_AUTHORING.md``:
  id = "{track_slug}/{lesson_slug}", track == id prefix, 1–8 objectives,
  step_outline steps carry the per-type required field (topic / patch_brief /
  task / clip_topic), and a text step may add a "diagram".

Prerequisites are expressed once, in ``PREREQ_EDGES`` (prerequisite → lesson, by
spec id), and resolved to UUIDs at seed time. The graph is a DAG rooted at
``synthesis-fundamentals/intro`` (asserted by the QA DAG check + the e2e test).
"""

