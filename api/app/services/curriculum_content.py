# --- Authoring helpers -------------------------------------------------------

def _text(topic: str, diagram: str | None = None) -> dict[str, Any]:
    s: dict[str, Any] = {"type": "text", "topic": topic}
    if diagram:
        s["diagram"] = diagram
    return s


def _demo(patch_brief: str) -> dict[str, Any]:
    return {"type": "demo", "patch_brief": patch_brief}


def _prompt(task: str) -> dict[str, Any]:
    return {"type": "prompt", "task": task}


def _reflect(topic: str) -> dict[str, Any]:
    return {"type": "reflection", "topic": topic}


def _clip(clip_topic: str) -> dict[str, Any]:
    return {"type": "audio-clip", "clip_topic": clip_topic}


def _lesson(
    track: str,
    slug: str,
    title: str,
    objectives: list[str],
    difficulty: str,
    steps: list[dict[str, Any]],
    *,
    minutes: int = 12,
    constraints: list[str] | None = None,
    description: str | None = None,
) -> dict[str, Any]:
    return {
        "id": f"{track}/{slug}",
        "track": track,
        "title": title,
        "objectives": objectives,
        "difficulty": difficulty,
        "prerequisites": [],  # authoritative edges live in PREREQ_EDGES
        "step_outline": steps,
        "constraints_during_prompts": constraints or [],
        "estimated_minutes": minutes,
        "description": description,
    }


